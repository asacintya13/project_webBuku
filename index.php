                    <div class="tab-pane fade show active" id="list-scifi" role="tabpanel" aria-labelledby="list-scifi-list">
                      Science fiction is a genre of fiction in which the stories often
                      tell about science and technology of the future. It is
                      important to note that science fiction has a relationship with
                      the principles of science—these stories involve partially truepartially fictitious laws or theories of science. It should not be
                      completely unbelievable, because it then ventures into the
                      genre fantasy.
                      The plot creates situations different from those of both the
                      present day and the known past. Science fiction texts also
                      include a human element, explaining what effect new
                      discoveries, happenings and scientific developments will have
                      on us in the future.
                      Science fiction texts are often set in the future, in space, on
                      a different world, or in a different universe or dimension. </div>

                    <div class="tab-pane fade" id="list-roman" role="tabpanel" aria-labelledby="list-roman-list"> In the strictest academic terms, a romance is a narrative genre in literature that involves a mysterious, adventurous, or spiritual story line where the focus is on a quest that involves bravery and strong values, not always a love interest. However, modern definitions of romance also include stories that have a relationship issue as the main focus.</div>

                    <div class="tab-pane fade" id="list-mys" role="tabpanel" aria-labelledby="list-mys-list"> Mystery (pronounced mis-tuh-ree, ) is a genre of literature whose stories focus on a puzzling crime, situation, or circumstance that needs to be solved. The term comes from the Latin mysterium, meaning “a secret thing.” stories can be either fictional or nonfictional, and can focus on both supernatural and non-supernatural topics. Many mystery stories involve what is called a “whodunit” scenario, meaning the mystery revolves around the uncovering a culprit or criminal.</div>

                    <div class="tab-pane fade" id="list-comics" role="tabpanel" aria-labelledby="list-comics-list"> A comic book or comicbook, also called comic magazine or simply comic, is a publication that consists of comics art in the form of sequential juxtaposed panels that represent individual scenes. Panels are often accompanied by descriptive prose and written narrative, usually, dialog contained in word balloons emblematic of the comics art form. Although comics has some origins in 18th century Japan, comic books were first popularized in the United States and the United Kingdom during the 1930s. The first modern comic book, Famous Funnies, was released in the US in 1933 and was a reprinting of earlier newspaper humor comic strips, which had established many of the story-telling devices used in comics.The term comic book derives from American comic books once being a compilation of comic strips of a humorous tone; however, this practice was replaced by featuring stories of all genres, usually not humorous in tone.</div>

                    <div class="tab-pane fade" id="list-hrr" role="tabpanel" aria-labelledby="list-hrr-list">In literature, horror (pronounced hawr-er)  is a genre of fiction whose purpose is to create feelings of fear, dread, repulsion, and terror in the audience—in other words, it develops an atmosphere of horror. The term’s definition emphasizes the reaction caused by horror, stemming from the Old French orror, meaning "to shudder or to bristle."</div>

                    <div class="tab-pane fade" id="list-adv" role="tabpanel" aria-labelledby="list-adv-list">A genre of fiction in which action is the key element, overshadowing characters, theme and setting. … The conflict in an adventure story is often man against nature. A secondary plot that reinforces this kind of conflict is sometimes included. In Allistair MacLean’s Night Without End, for example, the hero, while investigating a mysterious Arctic air crash, also finds himself dealing with espionage, sabotage and murde</div>

                    <div class="tab-pane fade" id="list-tr" role="tabpanel" aria-labelledby="list-tr-list">A novel intended to arouse feelings of excitement or suspense. Works in this genre are highly sensational, usually focusing on illegal activities, international espionage, sex and violence. A thriller is often a detective story in which the forces of good are pitted against the forces of evil in a kill-or-be-killed situation.</div>

                    <div class="tab-pane fade" id="list-fn" role="tabpanel" aria-labelledby="list-fn-list">Fantasy Fiction could be described as something that contains rudiments that are not realistic, such as magical powers, talking animals, etc. The genre is often characterized by a departure from the accepted rules by which individuals perceive the world around them; it represents that which is impossible (unexplained) and outside the parameters of our known, reality. Make-believe is what this genre is all about.

                      Another description of a Fantasy Novel is any book that contains unrealistic settings, or magic, often set in a medieval universe, or possibly involving mythical beings or supernatural forms as a primary element of the plot, theme, or setting. Something magical is almost always part of fantasy and magic may be seen in the setting or in the plot. It may even be practiced by the characters.</div>
